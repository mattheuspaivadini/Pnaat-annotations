# Lista
lista_exemplo = [22.5, 99.9, 59.9] # exemplo de lista
print(lista_exemplo)
lista_exemplo.append(23.0) # O valor 23.0 será acrescentado a lista
print(f'Lista pós mudança: {lista_exemplo}')
# Listas são mutáveis, por isso é possível adicionar ou remover elementos

# TUPLAS
tupla_exemplo = (40.75, -55.00) # exemplo de tupla
print(tupla_exemplo)
# A maior diferença entre listas e tuplas, é que as tuplas são imutáveis. Portanto, não é possível adicionar ou remover valores delas

# DICIONÁRIOS
# A principal diferença dos demais é que um dicionário possui uma CHAVE e VALOR
dicionario_ex = {"id": 101, "tipo": "temperatura", "valor": 24.6}
#São mutáveis assim como as listas

