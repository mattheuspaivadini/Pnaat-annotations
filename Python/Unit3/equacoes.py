# expressão matematica
a = 7
b = 3
c = 5
d = 2
equacao = ((a * b) + (c / d) - (a ** b))
print(f'Resposta da equação: {equacao}')

# expressão condicional
x = 10
y = 20
z = 30
expressao = (x < y and y > z) or (x + y == z and not (z == x * 3))
print(f'resposta da expressão: {expressao}')

# expressão relacional
m = 25
n = 35
p = 45
relacionais = ((m + n) > (p * 2)) and ((p / n) < m) or (n == m + p)
print(f'resposta da expressão: {relacionais}')